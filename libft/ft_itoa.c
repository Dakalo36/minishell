#include <string.h>
#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;

	str = ft_strnew(20) + 19;
	if (n >= 0 && str)
	{
		*--str = '0' + (n % 10);
		n = n / 10;
		while (str != NULL)
		{
			*--str = '0' + (n % 10);
			n = n / 10;
		}
	}
	else if (str)
	{
		*--str = '0' - (n % 10);
		n = n / 10;
		while (n != 0)
		{
			*--str = '0' - (n % 10);
			n = n / 10;
		}
		*--str = '-';
	}
	return (str);
}

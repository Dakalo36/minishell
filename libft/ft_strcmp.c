#include "libft.h"

int		ft_strcmp(const char *str1, const char *str2)
{
	int		i;

	i = 0;
	while (str1[i] != '\0' && str2[i] != '\0' && str1[i] == str2[i])
		i++;
	return ((int)(str1[i] - str2[i]));
}

#include <string.h>
#include "libft.h"

size_t	ft_strlen(const char *str)
{
	int		i;
	size_t	count;

	i = 0;
	count = 0;
	while (str[i] != '\0')
	{
		count++;
		i++;
	}
	return (count);
}
